							El Programa Escuelas de Tiempo Completo está destinado a lograr que los alumnos y alumnas de las escuelas públicas de educación básica cuenten con ambientes que les permitan el logro de mejores aprendizajes y de un desarrollo integral mediante la ampliación y uso eficaz de la jornada escolar y de mecanismos de mejora en el aprendizaje que hagan un alto a la deserción educativa y que favorezcan la retención escolar.

